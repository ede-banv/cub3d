#define ISALPHA (c >= '0' && c <= '9') || (c >= 'A'
#define NUM c <= 'Z') || (c >= 'a' && c <= 'z')

int	ft_isalnum(int c)
{
	if (ISALPHA && NUM)
		return (1);
	return (0);
}
